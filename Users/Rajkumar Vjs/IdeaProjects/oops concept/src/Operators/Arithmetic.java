package Operators;
import java.util.*;

public class Arithmetic {
    public static void main(String[] args) {
        int personOne = 12000, personTwo = 50000;
        System.out.println("a + b = " + (personOne+ personTwo));

        System.out.println("a - b = " + (personOne - personTwo));

        System.out.println("a * b = " + (personOne * personTwo));

        System.out.println("a / b = " + (personOne / personTwo));

        System.out.println("a % b = " + (personOne % personTwo));
    }
}


